    private boolean checkValidation(){
        if(name.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Name");
            name.grabFocus();
            return false;
        }else if(gender.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Select gender");
            return false;
        }else if(mobile.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Mobile");
            mobile.grabFocus();
            return false;
        }else if(address.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Address");
            address.grabFocus();
            return false;
        }else if(userType.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Select UserType");
            return false;
        }else if(email.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Email");
            email.grabFocus();
            return false;
        }else if(!isEmailValidPattern()){    
            JOptionPane.showMessageDialog(RegistrationForm.this, "Invalid Email Pattern");
            return false;
        }else if(RegistrationDao.isEmailExited(email.getText())){
           JOptionPane.showMessageDialog(RegistrationForm.this, "Email already Exist..");
           return false;
        }else if(pass.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Password");
            pass.grabFocus();
            return false;
        }else if(cpass.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Confirm Password");
            cpass.grabFocus();
            return false;
        }else if((pass.getText()).equals(cpass.getText())){
            return true;
        }else{
            JOptionPane.showMessageDialog(RegistrationForm.this, "Password not matched..");
        }
        return false;
}
    private boolean isEmailValidPattern(){
        String regex = "^(.+)@(\\S+)$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(email.getText());
        return matcher.matches();
    }
