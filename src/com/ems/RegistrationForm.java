    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if(checkValidation()){
            String uname = name.getText();
            String ugender = (String)gender.getSelectedItem();
            String umobile = mobile.getText();
            String uaddress = address.getText();
            String utype = (String)userType.getSelectedItem();
            String uemail = email.getText();
            String upass = pass.getText();
            Person person = null;
            Login login = new Login(0,uemail,upass);
            if(utype.equals("Manager")){
                person = new Manager(uname,ugender,umobile,uaddress,uemail,utype);               
            }else if(utype.equals("Client")){
                person = new Customer(uname,ugender,umobile,uaddress,uemail,utype);
            }
            boolean flag = RegistrationDao.save(person, login);
            if(flag){
                JOptionPane.showMessageDialog(RegistrationForm.this, "Registration successfull...");
            }else{
                JOptionPane.showMessageDialog(RegistrationForm.this, "something went wrong...");
            }
            reSet();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

